// Invocación de la función
mostrar('Pepe')

// Declaración de la funcion
// Hiting de funciones
function mostrar (n) {
    console.log(`Me han pasado ${n}`)
}

// Funcion === Objeto
// Función anónima
// Asignación de la función a una variable
let mostrarVariable = function (n) { 
    console.log(`Me han pasado ${n}`)
}

// Invocación de la función
mostrarVariable('Juan')